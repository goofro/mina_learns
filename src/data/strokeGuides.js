// Extract the starting [x, y] of an SVG path (the first M command)
export function pathStart(d) {
  const m = d.match(/M\s*([\d.]+)[\s,]+([\d.]+)/)
  return m ? [parseFloat(m[1]), parseFloat(m[2])] : [50, 50]
}
